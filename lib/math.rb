def addition(num1, num2)
  a = num1 + num2 
  return a 
end

def subtraction(num1, num2)
  s = num1 - num2 
  return s 
end

def division(num1, num2)
  d = num1/ num2
  return d
end

def multiplication(num1, num2)
  m = num1 * num2 
  return m 
end

def modulo(num1, num2)
  m = num1 % num2 
  return m 
end

def square_root(num)
  r = Math.sqrt(num)
  return r 
end